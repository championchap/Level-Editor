class TerrainEditor extends React.Component {
  componentDidMount() {
    const container = document.querySelector('#bramble-view')

    game.attachTo(container)
    game.disableContextMenu()
    game.setSize(this.props.width, this.props.height)
    game.setSmoothing(false)

    game.setUpdate(delta => {})

    let spritesheets = []

    game.setRender(() => {
      graphics.clear('#000000')
      graphics.tiles(
        0, // camera x
        0, // camera y
        allShapes, // tiles, the array
        spritesheets, // spritesheets, an array of spritesheets
        4, // scale
        8, // tileSize
        8 // tileSize
      )
    })

    Promise.all([assets.loadTerrain('./terrain/default.json')])
      .then(terrain => {
        spritesheets = terrain
        game.start()
      })
      .catch(err => {
        console.error(err)
      })
  }

  render() {
    return (
      <div id='bramble-pane'>
        <div id='bramble-view'></div>
      </div>
    )
  }
}

const mapStateToProps = state => {
  return {
    width: 500,
    height: 500
  }
}

module.exports = connect(mapStateToProps)(TerrainEditor)
